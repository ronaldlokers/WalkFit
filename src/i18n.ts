const messages = { en, nl } as const
export type Locale = keyof typeof messages
export const LOCALES: { id: Locale; label: string }[] = [
  { id: 'en', label: 'English' },
  { id: 'nl', label: 'Nederlands' },
]

const stored = localStorage.getItem('walkfit.lang')
export const locale = ref<Locale>(stored === 'nl' ? 'nl' : 'en')
watch(locale, (v) => localStorage.setItem('walkfit.lang', v), { flush: 'sync' })

// BCP-47 tags: SpeechSynthesis voices and toLocaleDateString both key off these.
export function localeTag(): string {
  return locale.value === 'nl' ? 'nl-NL' : 'en-US'
}

export function t(key: MessageKey, params?: Record<string, string | number>): string {
  let out: string = messages[locale.value][key] ?? en[key]
  if (params) {
    for (const [k, v] of Object.entries(params)) out = out.replaceAll(`{${k}}`, String(v))
  }
  return out
}
